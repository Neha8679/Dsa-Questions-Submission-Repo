class Solution {
  public:
    // Function to find pair with maximum sum
    int pairWithMaxSum(vector<int> &arr) {
        // Your code goes here
        int n=arr.size();
        int sum=0;
        int max_sum=arr[0];
        for(int i=0;i<n;i++){
            sum=sum+arr[i];
            if(sum>max_sum){
                max_sum=sum;
                
            }
            sum=arr[i];
            
        }
        return max_sum;
    }
};


